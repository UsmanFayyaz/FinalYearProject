        if (email.equals(sharedPref.getString("email", "")) && password.equals(sharedPref.getString("password", ""))) {
            onLoginSuccess();
            progressDialog.dismiss();
        } else {
            progressDialog.dismiss();
            onLoginFailed();
        }

